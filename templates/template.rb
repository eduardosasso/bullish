# frozen_string_literal: true

require 'mustache'
require './templates/element'
require './services/mjml'
require './services/minifier'
require 'date'

module Templates
  class Template
    attr_accessor :preheader, :premium

    def initialize(elements = [Element.title])
      @elements = elements
      @preheader = nil
      @premium = false
    end

    def body
      [
        Element.header(@premium),
        Element.spacer('15px'),
        sponsor,
        Element.divider,
        @elements,
        Element.divider,
        sponsor,
        Element.footer(@premium)
      ].flatten.compact.join(' ')
    end

    def compile
      data = Element::Html.new(
        preheader: @preheader,
        body: body
      )

      Element.html(data)
    end

    def sponsor
      return if @premium

      labels = [
        '<a href="https://app.adjust.com/q8qfi9f" style="color:#FFF"><b><u>An investing platform like no other</u></b></a><br/> On <b>Public.com</b>, you can buy stocks with any amount of money, see what others are investing in, and be part of a community of investors. <p><a href="https://app.adjust.com/q8qfi9f" style="color: #FFF">Get up to $50 in free stock on Bullish â†’</a></p>'
      ]

      Element.sponsor(labels.sample)
    end

    def to_html
      mjml = compile

      Services::Mjml.new(mjml).to_html
    end

    def self.edition(edition)
      all_elements = edition.title + edition.elements

      new(all_elements).tap do |t|
        t.preheader = edition.preheader
        t.premium = edition.premium?
      end
    end
  end
end
